package net.hep.ami.utility;

public class Tuple5<A, B, C, D, E>
{
	/*---------------------------------------------------------------------*/

	public final A x;
	public final B y;
	public final C z;
	public final D t;
	public final E u;

	/*---------------------------------------------------------------------*/

	public Tuple5(A _x, B _y, C _z, D _t, E _u)
	{
		x = _x;
		y = _y;
		z = _z;
		t = _t;
		u = _u;
	}

	/*---------------------------------------------------------------------*/
}
