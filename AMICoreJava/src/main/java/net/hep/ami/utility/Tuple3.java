package net.hep.ami.utility;

public class Tuple3<A, B, C>
{
	/*---------------------------------------------------------------------*/

	public final A x;
	public final B y;
	public final C z;

	/*---------------------------------------------------------------------*/

	public Tuple3(A _x, B _y, C _z)
	{
		x = _x;
		y = _y;
		z = _z;
	}

	/*---------------------------------------------------------------------*/
}
