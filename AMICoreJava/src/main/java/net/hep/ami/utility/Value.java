package net.hep.ami.utility;

import java.io.*;

import org.jetbrains.annotations.*;

public class Value<A> implements Serializable
{
	/*----------------------------------------------------------------------------------------------------------------*/

	private static final long serialVersionUID = -4379454079126814232L;

	/*----------------------------------------------------------------------------------------------------------------*/

	public A value;

	/*----------------------------------------------------------------------------------------------------------------*/

	@Contract(pure = true)
	public Value()
	{
		value = null;
	}

	/*----------------------------------------------------------------------------------------------------------------*/

	@Contract(pure = true)
	public Value(A _value)
	{
		value = _value;
	}

	/*----------------------------------------------------------------------------------------------------------------*/
}
